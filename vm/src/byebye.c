#include "../inc/vm.h"

void			vm_error(char *msg)
{
	ft_printf("{red}{b}ERROR: ");
	if (errno == 0)
		ft_putendl_fd(msg, 2);
	else
		perror(msg);
	ft_printf("{0}");
	exit(1);
}
